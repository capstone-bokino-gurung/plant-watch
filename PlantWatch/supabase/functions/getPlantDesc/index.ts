// Setup type definitions for built-in Supabase Runtime APIs
import "@supabase/functions-js/edge-runtime.d.ts";
import OpenAI from "OpenAI";
const client = new OpenAI();

Deno.serve(async (req) => {
  const { commonName } = await req.json()

  const description = await client.responses.create({
    model: "gpt-5.4-mini",
    input: `Provide a 2 sentence description followed by 2-4 sentences of care guidance for the plant with this common name: ${commonName}. Separate the paragraphs with a blank line.`
  });

  const data = {
    message: description.output_text,
  }

  return new Response(
    JSON.stringify(data),
    { headers: { "Content-Type": "application/json" } },
  )
})

